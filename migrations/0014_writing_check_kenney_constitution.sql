## Preamble

As a family of two parents and three children, five citizens of one small and quarrelsome republic, we wrote this down not because we already know how to live, but because we want to get better at it. A family isn''t a set of rules handed down from the top of the stairs. It''s a long argument between people who love each other and refuse, on principle, to stop asking questions. This isn''t scripture. Scripture can''t be changed; this can. We mean to keep it that way.

## The Articles

**I. Stay curious before you get certain.**
The first job of a Kenney is to want to know. Certainty is cheap and available on every corner. Curiosity costs something, because it means admitting you don''t understand yet. Better to ask a dumb question than to protect a comfortable ignorance. In this house, "I don''t know, let''s find out" isn''t a weakness. It''s how every interesting life starts.

**II. Do the work, and do it well.**
Eric Hoffer loaded ships by day and wrote philosophy by night, and never thought one was beneath the other. We respect effort that leaves a mark: the finished homework, the swept floor, the practiced scale, the job done right when no one''s watching. Talent is a rumor. Work is the proof. Be suspicious of shortcuts that skip the part where you actually get good at something.

**III. Try to see it from where the other person stands.**
Empathy isn''t a feeling that just happens to you. It''s something you practice. Before you judge, stand where the other person is standing and look at what they see from there: a brother, a stranger, a substitute teacher, the kid nobody sat with at lunch. Assume everyone you meet is fighting something you can''t see.

**IV. Think before you speak, and think again afterward.**
Being thoughtful is just curiosity aimed at consequences. Weigh your words. Consider what happens next, and after that. And change your mind when the facts change. That''s not flip-flopping, it''s growing up. An opinion you''ve never actually examined isn''t a belief. It''s a prejudice in a nicer coat.

**V. Tell the truth, especially when it costs you.**
Honesty is easy when it''s free. The test is the moment the truth will embarrass you, get you in trouble, or let someone down. We''d rather hear an uncomfortable truth than a convenient lie, and we promise to make truth-telling safe here by not punishing the guts it takes to do it.

**VI. Argue well, and don''t confuse an argument with a war.**
Disagreement is the sound of a family thinking out loud. Go after the idea, never the person. Grant that the other side might be partly right. And remember that raising your voice doesn''t strengthen your case. You can lose an argument at this table and lose nothing else: not your standing, not your dinner, not your welcome.

**VII. Be fair as if you didn''t know who you''d turn out to be.**
When you split anything, whether the last slice, the good seat, or the blame, do it as if you didn''t know which kid you were about to become. That''s the whole idea: a rule is only fair if you''d accept it before knowing whether it helped you or cost you. Fairness that only works when it favors you isn''t fairness. It''s just appetite with a lawyer.

**VIII. Keep your promises, or renegotiate them out loud.**
A Kenney''s word holds weight. If you can''t keep a promise, say so in advance, to the person you made it to. Don''t let it quietly fall apart and hope nobody notices. Being reliable isn''t glamorous, but it''s the thing that lets other people build their lives next to yours.

**IX. Be brave in small, everyday, unglamorous ways.**
Courage is rarely dramatic. It''s raising your hand when you''re not sure, apologizing first, sticking up for the unpopular kid, trying the hard piece, owning the mistake. Practice the small braveries, so the big ones, when they come, find you already in the habit.

**X. Look out for each other, and keep widening the circle.**
Family is the first loyalty, not the last. We have each other''s backs, no question, and then we look outward, because a household that only loves itself has mistaken a fortress for a home. Kindness that stops at the front door was never really kindness. Keep the door open.

**XI. Make things, and don''t apologize for beauty.**
Art is a force, not a decoration, and a bold, vivid imagination beats a tidy, well-behaved one. So make things: paintings, songs, arguments, jokes, meals, costumes, wild plans. Have strong tastes and defend them. Don''t sand down what''s original about you just to keep other people comfortable. Cleverness is nerve as much as brains.

**XII. Pay attention, and keep a notebook.**
Joan Didion built a whole life out of noticing: the exact detail, the thing everyone else walked right past. Keep a notebook. Write down what you saw, what was said, and how it felt, before it blurs. Creativity starts with attention, and attention is a form of love. And say what you actually mean, in plain and precise words, because sloppy language is usually sloppy thinking wearing a disguise.

**XIII. Think for yourself, and keep your sense of humor.**
Question the crowd, especially when it''s loud and certain. Distrust slogans and easy pieties, including your own. And laugh: a good laugh at your own expense keeps you honest, and humor is often the fastest way to tell the truth. Never be so impressed by authority that you forget to ask it a hard question.

## Article of Amendment: The Kenney Constitutional Convention

This is a living document, and living things change.

**§1. Who may vote.** As a family we have five citizens, each with one vote of equal weight: Matthew, Jenny, and each of the three children. A parent''s vote counts no more than a child''s. That''s on purpose.

**§2. Calling a Convention.** Any citizen, of any age, can call a Kenney Constitutional Convention at any time, for any reason, just by proposing it. A Convention can''t be refused. It can only be scheduled.

**§3. The threshold.** To add a new article, change one, or repeal one, a proposal needs **three-fifths of the vote**, meaning **three of the five citizens** in favor. No article is too old to change and none is too sacred to question, including this amendment process itself.

**§4. The record.** Every change gets written down, dated, and signed by everyone who voted, so future Kenneys can see not just what we decided but that we were willing to decide it again.

**§5. The one thing we don''t put to a vote.** That we love each other isn''t a proposal, an article, or up for ratification. It''s the ground the whole document stands on, and no Convention can repeal it.

